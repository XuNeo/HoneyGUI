static void gui_fps_cb(gui_img_t *img)
{
    int spf = gui_spf();
    int fps_num = 20;

    if (spf)
    {
        fps_num = 1000 / gui_spf();
    }

    if (fps_num > 99999)
    {
        fps_num = 99999;
    }

    gui_log("%d ", fps_num);
    sprintf(fps, "%d", fps_num);
    gui_text_content_set(t, fps, strlen(fps));
}

